package org.yes.cart.domain.entity.impl;


import org.yes.cart.domain.entity.Shop;
import org.yes.cart.domain.misc.Pair;
import org.yes.cart.util.MoneyUtils;

import java.math.BigDecimal;
import java.time.Instant;
import java.time.LocalDateTime;

/**
 * User: Igor Azarny iazarny@yahoo.com
 * Date: 27 0ct 2012
 * Time: 9:10 AM
 */
public class SkuPriceEntity implements org.yes.cart.domain.entity.SkuPrice, java.io.Serializable {

    private long skuPriceId;
    private long version;

    private String skuCode;
    private Shop shop;
    private String currency;
    private BigDecimal quantity;
    private boolean priceUponRequest;
    private BigDecimal regularPrice;
    private BigDecimal salePrice;
    private BigDecimal minimalPrice;
    private LocalDateTime salefrom;
    private LocalDateTime saleto;
    private Instant createdTimestamp;
    private Instant updatedTimestamp;
    private String createdBy;
    private String updatedBy;
    private String guid;
    private String tag;
    private String pricingPolicy;
    private String ref;
    private boolean autoGenerated;

    public SkuPriceEntity() {
    }


    public String getSkuCode() {
        return skuCode;
    }

    public void setSkuCode(final String skuCode) {
        this.skuCode = skuCode;
    }

    public Shop getShop() {
        return this.shop;
    }

    public void setShop(Shop shop) {
        this.shop = shop;
    }

    public String getCurrency() {
        return this.currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }

    public BigDecimal getQuantity() {
        return this.quantity;
    }

    public void setQuantity(BigDecimal quantity) {
        this.quantity = quantity;
    }

    @Override
    public boolean isPriceUponRequest() {
        return priceUponRequest;
    }

    @Override
    public void setPriceUponRequest(final boolean priceUponRequest) {
        this.priceUponRequest = priceUponRequest;
    }

    public BigDecimal getRegularPrice() {
        return this.regularPrice;
    }

    public void setRegularPrice(BigDecimal regularPrice) {
        this.regularPrice = regularPrice;
    }

    public BigDecimal getSalePrice() {
        return this.salePrice;
    }

    /** {@inheritDoc} */
    public Pair<BigDecimal, BigDecimal> getSalePriceForCalculation() {
        if (salePrice == null) {
            // No sale price is set only regular should be used
            return new Pair<>(regularPrice, null);
        } else if (MoneyUtils.isFirstBiggerThanOrEqualToSecond(salePrice, regularPrice)) {
            // Overpriced (or sale is the same as regular)
            return new Pair<>(salePrice, null);
        }
        return new Pair<>(regularPrice, salePrice);
    }

    public void setSalePrice(BigDecimal salePrice) {
        this.salePrice = salePrice;
    }

    public BigDecimal getMinimalPrice() {
        return this.minimalPrice;
    }

    public void setMinimalPrice(BigDecimal minimalPrice) {
        this.minimalPrice = minimalPrice;
    }

    public LocalDateTime getSalefrom() {
        return this.salefrom;
    }

    public void setSalefrom(LocalDateTime salefrom) {
        this.salefrom = salefrom;
    }

    public LocalDateTime getSaleto() {
        return this.saleto;
    }

    public void setSaleto(LocalDateTime saleto) {
        this.saleto = saleto;
    }

    public Instant getCreatedTimestamp() {
        return this.createdTimestamp;
    }

    public void setCreatedTimestamp(Instant createdTimestamp) {
        this.createdTimestamp = createdTimestamp;
    }

    public Instant getUpdatedTimestamp() {
        return this.updatedTimestamp;
    }

    public void setUpdatedTimestamp(Instant updatedTimestamp) {
        this.updatedTimestamp = updatedTimestamp;
    }

    public String getCreatedBy() {
        return this.createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }

    public String getUpdatedBy() {
        return this.updatedBy;
    }

    public void setUpdatedBy(String updatedBy) {
        this.updatedBy = updatedBy;
    }

    public String getGuid() {
        return this.guid;
    }

    public void setGuid(String guid) {
        this.guid = guid;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(final String tag) {
        this.tag = tag;
    }

    public String getPricingPolicy() {
        return pricingPolicy;
    }

    public void setPricingPolicy(final String pricingPolicy) {
        this.pricingPolicy = pricingPolicy;
    }

    public String getRef() {
        return ref;
    }

    public void setRef(final String ref) {
        this.ref = ref;
    }

    @Override
    public boolean isAutoGenerated() {
        return autoGenerated;
    }

    @Override
    public void setAutoGenerated(final boolean autoGenerated) {
        this.autoGenerated = autoGenerated;
    }

    public long getSkuPriceId() {
        return this.skuPriceId;
    }

    public long getId() {
        return this.skuPriceId;
    }

    public void setSkuPriceId(long skuPriceId) {
        this.skuPriceId = skuPriceId;
    }

    public long getVersion() {
        return version;
    }

    public void setVersion(final long version) {
        this.version = version;
    }
}


